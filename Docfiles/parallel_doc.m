%% parallel
% Choose between simulating with or without parallel computing.
%% Syntax
% c.parallel = boolean
%% Description
% In _c.parallel = boolean_, _boolean_ can be set to _true_ (enable parallel computing), or _false_ (disable parallel computing). 
% _c.parallel_ controls whether multiple simulation runs are performed in parallel on a multicore computer system or in a sequential manner.
% *Note*: _c.parallel_ can only be set to _true_ if the MATLAB® Parallel
% Computing Toolbox is installed.

