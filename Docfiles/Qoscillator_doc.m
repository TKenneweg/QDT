%% Qoscillator
% A quantum harmonic oscillator. Most commonly used to model a light field or a plasmon inside a cavity.
%% Syntax
% Qoscillator(n,gap)
%% Description
% Qoscillator(n,gap) _n_ denotes the number of levels that is used in the simulation. In order to avoid mistakes
% this number should be such that the highest level has a very low occupation at all times. In principle the quantum 
% harmonic oscillator has an infinite number of levels. _gap_ denotes the energy gap between two adjacent levels in atomic units.